document.getElementById('b1').onclick = function(){
    if(document.getElementById('a1').style.display == 'block'){
        document.getElementById('a1').style.display = 'none';
        document.getElementById('b1').style.backgroundImage = 'url(assets/images/icon-plus.svg)';
    }else{
        document.getElementById('a1').style.display = 'block';
        document.getElementById('a1').style.marginTop = '20px';
        document.getElementById('b1').style.backgroundImage = 'url(assets/images/icon-minus.svg)';
    }
};
